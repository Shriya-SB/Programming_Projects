// There are 3 types of variable const, let and var
// const - Variable cannot be redeclared nor be updated
// let - variable cannot be redeclared but can be updated
// var - variable can be updated and redeclared

const c = 45
console.log(c);
let a = 2
console.log(a);
a = 4
console.log(a);
var b = 50
console.log(b);
var b = "40"
console.log(b);
b = "Navneet"
console.log(b);