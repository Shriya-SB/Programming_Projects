// Here we will explain all main concepts of Math objects.

// Random method
// This will print the number between 0 - 1 a random value.
const a = Math.random()
console.log(a);

// Ceil method
// This will round of the number up and prints the value
const b = Math.ceil(5.2)
console.log(b);

// Floor method
// This will round of the number down and prints the value
const c = Math.floor(5.8)
console.log(c);

// Round Method
// Just round of the number fairly according to decimal value.
const d = Math.round(7.1)
console.log(d);

// Pow method
// This method will help us to substitude exponent problems and returns the value.
const e = Math.pow(11, 4)
console.log(e);

// Max method
// In a group of number finds the biggest number.
const f = Math.max(10, 12, 94, 79.55133)
console.log(f);

// Min method
// In a group of number finds the smallest number.
const g = Math.min(10, 12, 94, 79.55133)
console.log(g);